#define _CRT_SECURE_NO_WARNINGS 1

#include <stdio.h>

int main()
{
	int a = 9;
	//00000000 00000000 00000000 00001001 -补码
	//0(s位) 00000000(e位) 0000000 00000000 00001001(M位)
	//e为全0,所以(-1)^0*0.00000000000000000001001*2^-126
	float* pa =(float*) & a; 

	printf("%d\n", a);//9

	printf("%lf\n", *pa);//0.000000
	//(-1)^0*0.00000000000000000001001*2^-126//0

	*pa = 9.0;
	//1001.0
	//1.001*2^3
	//0 +3+127 00100000000000000000000
	//0 10000010 00100000000000000000000
	//01000001000100000000000000000000 补码形式
	printf("%d\n", a);
	printf("%lf\n", *pa);
	return 0;
}